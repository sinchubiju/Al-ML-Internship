import math

# Square root
print(math.sqrt(16))

# Power
print(math.pow(2, 3))

# Rounding
print(math.ceil(2.3))
print(math.floor(2.9))

# Pi value
print(math.pi)


import random
# Random number
print(random.randint(1, 10))

# Random choice
print(random.choice([10, 20, 30]))

# Random float
print(random.random())

# List of random numbers
numbers = [random.randint(1, 100) for i in range(5)]
print(numbers)